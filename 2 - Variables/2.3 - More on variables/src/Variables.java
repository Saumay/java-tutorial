class Variables
{
	public static void main(String a[])
	{
		double num1 = 2.2;
		double num2 = 3.2;
		System.out.println(num1 + num2);
		
		float num3 = 12.2f;
		float num4 = 13.2f;
		System.out.println(num3 + num4);
		
		char c = 'A';
		System.out.println("Ascii value of " + c + " = " + (int)c);
		
		int i = 110;
		System.out.println("Ascii character for integer " + i + " = " + (char)i);
	}
}